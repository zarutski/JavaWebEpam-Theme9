package by.epamtc.topic.nine;

public class EvenOddPair {

    private final String even;
    private final String odd;

    public EvenOddPair(String even, String odd) {
        this.even = even;
        this.odd = odd;
    }

    public String getEven() {
        return even;
    }

    public String getOdd() {
        return odd;
    }
}
